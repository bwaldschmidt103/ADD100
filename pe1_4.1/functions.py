
# Define the function to calculate the area of a square
def square(side):
    area = side * side
    print(f"The area of the square is {str(area)} square units.")

#Define the function to calculate the area of a circle
def circle(radius):
    pi = 3.14
    area = pi * radius * radius
    print(f"The area of the circle is {str(area)} square units.")

# Test the functions with example values
square(18)
circle(15)
